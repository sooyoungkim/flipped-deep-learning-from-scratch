# coding: utf-8
import sys, os
sys.path.append(os.pardir)  # 부모 디렉터리의 파일을 가져올 수 있도록 설정
import numpy as np
from dataset.mnist import load_mnist
from PIL import Image


def img_show(img):
    pil_img = Image.fromarray(np.uint8(img))    # numpy array로 저장된 이미지 데이터를 PIL용 데이터 객체로 변환
    pil_img.show()


# 데이터 가져오기
# unpacking : (훈련 이미지, 훈련 레이블), (시험 이미지, 시험 레이블)
(x_train, t_train), _ = load_mnist(flatten=False, normalize=False)
print(x_train.shape)    # (60000, 1, 28, 28)
print(t_train.shape)    # (60000,)     -> 6만개의 레이블 데이터


# 훈련용 데이터 사용
img = x_train[0]    # training 이미지 하나 가져오기
label = t_train[0]  # training 라벨 하나 가져오기
print(img)          # (맨 아래 주석처리된 배열 확인)
print(label)        # 5

print(img.shape)            # (1, 28, 28)
img = img.reshape(28, 28)   # (28 X 28)로 형상 변형
print(img.shape)            # (28, 28)

img_show(img)


