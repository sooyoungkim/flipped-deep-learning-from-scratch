"""출력층 함수 : 항등 함수

    :parameter
    ----------------
    x : 입력 데이터

    :return
    ----------------
    x > 0 이면 1
    나머지는    0

"""
def step_function(x):
    return np.array(x > 0, dtype=np.int)


data = np.array([-1.0, 1.0, 2.0])
print(data)    # [-1.  1.  2.]

